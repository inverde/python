# Looping lists
students = [
    'Luis Rodolfo',
    'Luis Miguel',
    'Manuel Alejandro'
]

# Use For Loops to loop throught lists
for student in students:
    print(student)

print()
for i in range(len(students)):
    print(i, students[i])

# Printing all the items individually
print()
print(students[0])
print(students[1])
print(students[2])

